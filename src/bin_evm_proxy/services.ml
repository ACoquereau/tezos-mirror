open Tezos_rpc

let version_service =
  Service.get_service
    ~description:"version"
    ~query:Query.empty
    ~output:Data_encoding.string
    Path.(root / "version")

let version dir =
  Directory.register0 dir version_service (fun () () ->
      Format.printf "Version\n%!" ;
      Lwt.return_ok Tezos_version.Bin_version.version_string)

let directory = Directory.empty |> version
